package main

import "fmt"

func main() {
	var i int
	var f float32
	var b bool
	var s string

	//caso a variável seja criada e não seja declarado valor a mesmo a própria linguage atribui

	fmt.Printf("inteiro: %v\n", i)
	fmt.Printf("float: %v\n", f)
	fmt.Printf("boolean: %v\n", b)
	fmt.Printf("str: %v\n", s)
}
